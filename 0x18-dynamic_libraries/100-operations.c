
/**
 * add - performs addition on two digits
 *
 * @a: first digit
 * @b: second digit
 *
 * Return: Returns the result of the operation.
 *
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - performs subtraction on two digits
 *
 * @a: first digit
 * @b: second digit
 *
 * Return: Returns the result of the operation.
 *
 */


int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - performs multiplication on two digits
 *
 * @a: first digit
 * @b: second digit
 *
 * Return: Returns the result of the operation.
 *
 */


int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - performs division on two digits
 *
 * @a: first digit
 * @b: second digit
 *
 * Return: Returns the result of the operation.
 *
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - performs modulus on two digits
 *
 * @a: first digit
 * @b: second digit
 *
 * Return: Returns the result of the operation.
 *
 */

int mod(int a, int b)
{
	return (a % b);
}
